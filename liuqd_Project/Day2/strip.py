names = "\n   LiuQuandong   \n"
print("============")
print(names,"=======")
print("============")

print("\nstrip是去掉两边的空格和回车")
print("============")
print(names.strip(),"=======")
print("============")

print("\nlstrip是去掉左边的空格和回车")
print("============")
print(names.lstrip(),"=======")
print("============")

print("\nrstrip是去掉右边的空格和回车")
print("============")
print(names.rstrip(),"=======")
print("============")



